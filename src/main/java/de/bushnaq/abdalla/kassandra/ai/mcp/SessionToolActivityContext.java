package de.bushnaq.abdalla.kassandra.ai.mcp;

import lombok.Setter;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;
import java.util.function.Consumer;

/**
 * Session-scoped ToolActivityContext implementation.
 * Buffers activity messages and can notify a listener (e.g., for UI streaming).
 */
public class SessionToolActivityContext implements ToolActivityContext {
    @Setter
    private       Consumer<String> activityListener;
    private final List<String>     activityLog = Collections.synchronizedList(new ArrayList<>());

    @Override
    public void reportActivity(String message) {
        activityLog.add(message);
        if (activityListener != null) {
            activityListener.accept(message);
        }
    }

}
